    private void anggotaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_anggotaTableMouseClicked
        // TODO add your handling code here:
        clearForm();
        editButton.setVisible(true);
        deleteButton.setVisible(true);
        saveButton.setVisible(false);
        PreparedStatement ps;
        ResultSet rs;
        int row = anggotaTable.getSelectedRow();
        String sql = "SELECT * FROM member WHERE NIM = ?";
        String NIM = anggotaTable.getValueAt(row, 1).toString();
        if(row!=-1){
            try {
                ps = Config.getConnection().prepareStatement(sql);
                ps.setString(1, NIM);
                rs = ps.executeQuery();
                if(rs.next()){
                    Anggota aggt = new Anggota();
                    aggt.setNim(rs.getString("nim"));
                    aggt.setNama(rs.getString("nama"));
                    aggt.setGender(rs.getString("gender"));
                    aggt.setAngkatan(rs.getInt("angkatan"));
                    aggt.setRole(rs.getString("role"));
                    aggt.setKeanggotaan(rs.getString("keanggotaan"));
                    
                    nimTextField.setText(aggt.getNim());
                    nameTextField.setText(aggt.getNama());
                    if("L".equals(aggt.getGender())){
                        jkComboBox.setSelectedIndex(0);
                    }else{
                        jkComboBox.setSelectedIndex(1);
                    }
                    angkatanSpinner.setValue(aggt.getAngkatan());
                    switch(aggt.getRole()){
                        case "Vokal" -> roleComboBox.setSelectedIndex(0);
                        case "Gitar" -> roleComboBox.setSelectedIndex(1);
                        case "Piano" -> roleComboBox.setSelectedIndex(2);
                        case "Bass" -> roleComboBox.setSelectedIndex(3);
                        case "Drum" -> roleComboBox.setSelectedIndex(4);
                        default -> roleComboBox.setSelectedIndex(0);
                    }
                    switch(aggt.getKeanggotaan()){
                        case "Ketua" -> memberComboBox.setSelectedIndex(0);
                        case "Wakil Ketua" -> memberComboBox.setSelectedIndex(1);
                        case "Sekretaris" -> memberComboBox.setSelectedIndex(2);
                        case "Bendahara" -> memberComboBox.setSelectedIndex(3);
                        case "PSDM" -> memberComboBox.setSelectedIndex(4);
                        case "HPD" -> memberComboBox.setSelectedIndex(5);
                        case "Perlengkapan" -> memberComboBox.setSelectedIndex(6);
                        case "Anggota" -> memberComboBox.setSelectedIndex(7);
                        default -> memberComboBox.setSelectedIndex(0);
                    }
                }
                
            } catch (Exception ex) {
                System.err.println(ex);
            }
        }
    }//GEN-LAST:event_anggotaTableMouseClicked

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = anggotaTable.getSelectedRow();
        if (row != -1) {
            String NIM = anggotaTable.getValueAt(row, 1).toString();
            try {
                Database.getInstance().deleteAnggota(NIM);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                loadTableData();
                clearForm();
            } catch (SQLException ex) {
//                System.err.println(ex);
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
                }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

