    private void loadTableData(){
        DefaultTableModel dtm = (DefaultTableModel)
        anggotaTable.getModel();
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        saveButton.setVisible(true);
        deleteButton.setVisible(false);
        editButton.setVisible(false);
        try {
        //isi tabel
            for(Anggota aggt: Database.getInstance().getListAnggota()){
                dtm.addRow(new Object[]{aggt.getAngkatan(),aggt.getNim(),aggt.getNama(),aggt.getRole(),aggt.getKeanggotaan()});
            
        }
        } catch (SQLException ex) {
            System.err.println(ex);
            JOptionPane.showMessageDialog(this, "Gagal mengambil data", "Gagal", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
